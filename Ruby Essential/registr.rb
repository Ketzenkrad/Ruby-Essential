#Методы регистра.

#capitalize - переводит в верхний регистр первый символ
str0 = "первая, строка"
puts str0.capitalize

#upcase - каждый символ в верхн.регистр
str1 = "ВТОРАЯ, строка"
puts str1.upcase

#downcase - каждый символ в нижний регистр
str2 = "ТРЕТЬЯ, СТРОКА"
puts str2.downcase

#swapcase - реверс регистров
str3 = "ЧетВЕРТАЯ, СТроКа"
puts str3.swapcase

